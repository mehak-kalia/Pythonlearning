def add(num1, num2):
    sum = num1 + num2
    return sum # Instead of empty returns, we can return back the data if we wish to


result = add(10, 20)
print("result is:", result)

result = add(2.2, 4.4)
print("result is:", result)

result = add(12, -19)
print("result is:", result)

# Functions can be either returning data or not returning data !!